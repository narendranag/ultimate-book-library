def write_batch(batch_dir: Path, name: str, books_data: list):
    books = []
    for item in books_data:
        if len(item) == 6:
            title, author, year, genres, pages, lang = item
        elif len(item) == 5:
            title, author, year, genres, pages = item
            lang = "en"
        elif len(item) == 4:
            title, author, year, genres = item
            lang = "en"
            pages = None
        else:
            title, author, year = item[:3]
            genres = []
            lang = "en"
            pages = None
        books.append(make_book(title, author, year, genres, lang, pages))

    out_path = batch_dir / f"{name}.json"
    with open(out_path, "w") as f:
        json.dump(books, f, indent=2, ensure_ascii=False)
    print(f"  {name}: {len(books)} books")
    return len(books)


