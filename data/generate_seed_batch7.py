def write_batch(batch_dir, name, books):
    out_path = batch_dir / f"{name}.json"
    with open(out_path, "w") as f:
        json.dump(books, f, indent=2, ensure_ascii=False)
    print(f"  {name}: {len(books)} books")
    return len(books)

