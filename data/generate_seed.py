# ISBN-13 prefix for generating consistent fake ISBNs from titles
# We use a deterministic hash so the same title always gets the same ISBN


def make_isbn13(title: str, author: str) -> str:
    """Generate a deterministic ISBN-13-like identifier from title+author."""
    h = hashlib.md5(f"{title}|{author}".encode()).hexdigest()
    digits = "978" + "".join(str(int(c, 16) % 10) for c in h[:9])
    # Calculate check digit
    total = 0
    for i, d in enumerate(digits):
        total += int(d) * (1 if i % 2 == 0 else 3)
    check = (10 - (total % 10)) % 10
    return digits + str(check)


def make_isbn10(isbn13: str) -> str:
    """Derive ISBN-10 from ISBN-13."""
    digits = isbn13[3:12]
    total = sum(int(d) * (10 - i) for i, d in enumerate(digits))
    check = (11 - (total % 11)) % 11
    return digits + ("X" if check == 10 else str(check))


def make_book(title, authors, year=None, genres=None, lang="en", pages=None):
    """Create a book dict with generated ISBNs."""
    if isinstance(authors, str):
        authors = [authors]
    isbn13 = make_isbn13(title, authors[0])
    isbn10 = make_isbn10(isbn13)
    book = {
        "title": title,
        "authors": authors,
        "isbn_13": isbn13,
        "isbn_10": isbn10,
        "genres": genres or [],
        "language": lang,
        "source": "seed",
    }
    if year:
        book["year_published"] = year
    if pages:
        book["page_count"] = pages
    return book


