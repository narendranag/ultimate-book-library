def write_batch(books_data, filename, data_dir):
    """Write a list of (title, authors, year, genres, pages) tuples as JSON."""
    books = []
    for item in books_data:
        if len(item) == 5:
            title, authors, year, genres, pages = item
        else:
            title, authors, year, genres = item[:4]
            pages = None

        books.append(make_book(title, authors, year, genres, pages=pages))

    path = data_dir / "batches" / filename
    with open(path, "w") as f:
        json.dump(books, f, indent=2, ensure_ascii=False)
    print(f"  {filename}: {len(books)} books")
    return len(books)


